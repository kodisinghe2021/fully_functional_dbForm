    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int x = table.getRowCount();
        try {
            for (int i = 0; i < x; i++) {
                invNO = table.getValueAt(i, 0).toString();
                stuID = table.getValueAt(i, 1).toString();
                payT = table.getValueAt(i, 2).toString();
                payF = table.getValueAt(i, 3).toString();
                pam = table.getValueAt(i, 4).toString();
                
                Connection c = DB.getConnection();
                PreparedStatement save = c.prepareStatement("INSERT INTO invoice(inv_no,stu_id,pay_t,pay_f,pay)VALUES('"+invNO+"','"+stuID+"','"+payT+"','"+payF+"','"+pam+"')");
                save.executeUpdate();
                save.close();
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        JOptionPane.showMessageDialog(this, "Save");
        this.dispose();
                new Invoice().setVisible(true);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel de = (DefaultTableModel) table.getModel();
        int as =table.getRowCount();
        for (int ax = 0; ax < as; ax++) {
            de.removeRow(0);
        }
        try {
            ResultSet r = DB.query("SELECT * FROM invoice WHERE inv_no = '"+inv_no.getText()+"'");
            while(r.next()){
            Vector v = new Vector();
            v.add(r.getString("inv_no"));
            v.add(r.getString("stu_id"));
            v.add(r.getString("pay_T"));
            v.add(r.getString("pay_f"));
            v.add(r.getString("pay"));
            de.addRow(v);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            JRTableModelDataSource pp = new JRTableModelDataSource(table.getModel());
            
            String reportSource ="report\\myreport.jrxml";
            Map<String,Object> params = new HashMap<String, Object>();
            params.put("sub_tot", sub_tot.getText());
            
            JasperReport jasperReport = JasperCompileManager.compileReport(reportSource);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, params, pp);
            
            //Print View
            JasperViewer.viewReport(jasperPrint,false);
            //Direct Print
           // JasperPrintManager.printReport(jasperPrint, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

