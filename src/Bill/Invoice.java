    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        invNO = inv_no.getText();
        stuID = stu_id.getText();
        payT = pay_t.getSelectedItem().toString();
        payF = pay_f.getSelectedItem().toString();
        pam = payment.getText();
        
        if (invNO.equals("")||invNO==null) {
            JOptionPane.showMessageDialog(this, "Enter Invoice NUmber");
        }else if (stuID.equals("")||stuID==null) {
            JOptionPane.showMessageDialog(this, "Enter STU_ID");
        }else if (payT.equals("-- Select --")) {
            JOptionPane.showMessageDialog(this, "Select Payment Type");
        }else if (payF.equals("-- Select --")) {
            JOptionPane.showMessageDialog(this, "Selcet Payment For");
        }else if (pam.equals("0")) {
            JOptionPane.showMessageDialog(this, "Enter Payment");
        }else{
        Vector v = new Vector();
        v.add(invNO);
        v.add(stuID);
        v.add(payT);
        v.add(payF);
        v.add(pam);
        
            DefaultTableModel de = (DefaultTableModel) table.getModel();
            de.addRow(v);
            
            try {
                pay = Integer.parseInt(pam);
                subtot = Integer.parseInt(sub_tot.getText());
                subtot = pay+subtot;
                sub_tot.setText(subtot+"");
                
            } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int i = table.getSelectedRow();
        invNO = table.getValueAt(i, 0).toString();
        stuID = table.getValueAt(i, 1).toString();
        payT = table.getValueAt(i, 2).toString();
        payF = table.getValueAt(i, 3).toString();
        pam = table.getValueAt(i, 4).toString();
        
        inv_no.setText(invNO);
        stu_id.setText(stuID);
        pay_t.setSelectedItem(payT);
        pay_f.setSelectedItem(payF);
        payment.setText(pam);
    }//GEN-LAST:event_tableMouseClicked

