void save(){
       try {
            Connection c = DB.getConnection();
            PreparedStatement save = c.prepareStatement("INSERT INTO registration(stu_id,fname,lname,nic,add1,add2,add3,tpm,tph)VALUES('"+stu_id.getText()+"','"+fname.getText()+"','"+lname.getText()+"','"+nic.getText()+"','"+add1.getText()+"','"+add2.getText()+"','"+add3.getText()+"','"+tpm.getText()+"','"+tph.getText()+"')");
            save.executeUpdate();
            save.close();
            
            JOptionPane.showMessageDialog(this, "Your data is Saved", "Save", JOptionPane.INFORMATION_MESSAGE);
            
            this.dispose();
            new Registration().setVisible(true);
        } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
}
