    void autoCount(){
        int coun=0,cll;
        Vector ew = new Vector();
        
        try {
            ResultSet rs = DB.query("SELECT * FROM registration");
            while(rs.next()){
            ew.add(rs.getString("stu_id"));
            }
        } catch (Exception e) {
        }
        int y = ew.size();
        
        try {
            for(int u = 0;u<y;u++){
            String ff = (String) ew.get(u);
            cll = Integer.parseInt(ff);
            if(coun<cll){
            coun= cll;
            }
            }
        } catch (Exception e) {
        }
        
        coun = coun+1;
        stu_id.setText(coun+"");
    }
